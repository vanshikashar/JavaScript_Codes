console.log("Using a For loop");
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

console.log("Using a While loop");
let j = 1;
while (j <= 5) {
    console.log(j);
    j++;
}

console.log("Using a do while loop")
let k = 1;
do {
    console.log(k);
    k++;
}while(k <= 5);    